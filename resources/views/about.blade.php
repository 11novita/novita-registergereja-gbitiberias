            <div id="faq3" class="collapse" data-parent=".faq-list">
              <p>
               Bahan bacaan Renungan Harian Kristen kali ini diambil dari Kitab Yohanes 16:12-24.
               Aku berkata kepadamu: seesungguhnya kamu akan menangis dan meratap, tetapi dunia akan bergembira; kamu akan berdukacita, tetapi dukacitamu akan berubah menjadi sukacita, (Yohanes 16:20)

               Ucapan Yesus membuat bingung para murid. Mereka tidak mengerti apa maksud-Nya Ia akan pergi dan mereka tidak akan melihat dia. Namun kemudian bisa kembali dan melihat dia ?(16-19). Hal ini  mempertegas ucapan Yesus kepada Nikodemus bahwa seorang perlu dilahirkan baru ditolong Roh agar dapat memahami kebenaran rohani.  Itu sebabnya, Yesus mengutus Roh Kudus agar mereka mengerti kebenaran Rohani tersebut dan memuliakan Yesus(13-15).

              </p>
            </div>
          </li>

          <li data-aos="fade-up" data-aos-delay="400">
            <a data-toggle="collapse" href="#faq4" class="collapsed">Berilah Tempat Bagi Roh Kudus di Hidupmu<i class="icofont-simple-up"></i></a>
            <div id="faq4" class="collapse" data-parent=".faq-list">
              <p>
                Bahan Bacaan Alkitab : Kis : 19:1-12
                Dan ketika Paulus menumpangkan tangan di atas mereka, turunlah Roh Kudus ke atas mereka , dan mulailah mereka berkata-kata dalam bahasa roh dan bernubuat.(Kis:19:6)

                Salah satu kerinduan dari mereka yang mengaku percaya pada Tuhan adalah mereka memiliki perubahan diri dan mampu mengatasi pergumulan hidup secara luar biasa. Dengan kata lain, mereka mengaharapkan adanya nilai tambah terhadap Tuhan dan Tuhan mau memakai mereka melakukan sesuatu “mujizat” dalam hidup mereka. Namun sayangnya, banyak yaang akhirnya kecewa dan sampai menyangkali imannya karena harapan dimaksud tidak tercapai.

                Percakapan antara Paulus dengan beberapa orang percaya di Efesus, rupanya menjelaskan hal yang sama. Sebelum Paulus memasuki Efesus, ternyata misi pekabaran injil telah sampai di daerah itu dengan didapatinya sejumlah murid (pengikut) Kristus.
